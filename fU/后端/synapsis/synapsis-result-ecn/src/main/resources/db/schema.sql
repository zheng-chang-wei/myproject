CREATE TABLE t_header_setting(
id BIGINT NOT NULL generated by default as identity,
setting_id INT,
length INT,
data_length INT,
group_id INT,
endian varchar(20),
PRIMARY KEY(id)
);

CREATE TABLE t_common_segment(
id BIGINT NOT NULL generated by default as identity,
segment_setting_id BIGINT,
index INT,
name varchar(20),
data_type varchar(20),
byte_len INT,
type INT,
property varchar(20),
variable_id BIGINT,
PRIMARY key(id)
);

CREATE TABLE t_result_setting(
id BIGINT NOT NULL generated by default as identity,
setting_id INT,
com_id BIGINT,
cycle varchar(20),
PRIMARY KEY(id)
);

CREATE TABLE t_algorithm_index(
id BIGINT NOT NULL generated by default as identity,
index_setting_id BIGINT,
index INT,
code INT,
PRIMARY KEY(id)
);