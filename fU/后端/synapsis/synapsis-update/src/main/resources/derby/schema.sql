	PACKAGE_NAME VARCHAR(50) NOT NULL,
	CPU_VERSION VARCHAR(50) NOT NULL,
	MVB_VERSION VARCHAR(50) NOT NULL,
	ECN_VERSION VARCHAR(50) NOT NULL,
	AD1_VERSION VARCHAR(50) NOT NULL,
	AD2_VERSION VARCHAR(50) NOT NULL,
	AD3_VERSION VARCHAR(50) NOT NULL,
	PHM_IMX8_VERSION VARCHAR(50) NOT NULL,
	PHM_TX2_VERSION VARCHAR(50) NOT NULL,
	PHM_AGX_VERSION VARCHAR(50) NOT NULL,
	UPLOAD_TIME TIMESTAMP NOT NULL,
	EFFECTIVE_TIME TIMESTAMP,
