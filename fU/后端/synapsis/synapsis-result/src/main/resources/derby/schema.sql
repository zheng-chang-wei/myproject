CREATE TABLE t_algorithm_result
(
id BIGINT NOT NULL generated by default as identity,
name varchar(16),
code INT,
file_type INT,
algorithm VARCHAR(100),
subsystem VARCHAR(50),
header_id BIGINT,
algorithm_header_id BIGINT,
timestamp TIMESTAMP,
value INT,
PRIMARY key(id)
);

CREATE TABLE t_common_header(
id BIGINT NOT NULL generated by default as identity,
system_time TIMESTAMP,
longi_degree INT,
longi_minute DOUBLE,
longi_direction VARCHAR(1),
lati_degree INT,
lati_minute DOUBLE,
lati_direction VARCHAR(1),
version varchar(10),
crc INT,
PRIMARY KEY(id)
);

CREATE TABLE t_algorithm_header(
id BIGINT NOT NULL generated by default as identity,
header_type INT,
algorithm_id INT,
supplier INT,
version varchar(10),
life_signal INT,
carriage INT,
terminal INT,
device_seq INT,
subsystem INT,
crc INT,
PRIMARY key(id)
);